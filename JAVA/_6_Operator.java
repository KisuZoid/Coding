public class _6_Operator {
    public static void main(String[] args){
        // Assignment Operators
        int x = 10;
        System.out.println("x = " + x);
        x += 5;
        System.out.println("10 + 5 = " + x + "\n");

        int y;

        y = 10;
        System.out.println("y = " + y);
        y+=3;
        System.out.println("y+=3 => y=y+3 i.e. "+ y);

        y = 10;
        System.out.println("y = " + y);
        y-=3;
        System.out.println("y-=3 => y=y-3 i.e. "+ y);

        y = 10;
        System.out.println("y = " + y);
        y*=3;
        System.out.println("y*=3 => y=y*3 i.e. "+ y);

        double z = 10;
        System.out.println("y = " + z);
        z/=3;
        System.out.println("y/=3 => y=y/3 i.e. "+ z);

        y = 10;
        System.out.println("y = " + y);
        y%=3;
        System.out.println("y%=3 => y=y%3 i.e. "+ y);

    }
}

/* Assignment operator:-
 * = : Assignment operator : assign left side value to the right side value.
 * += : x+=3 : same as x=x+3
 * -= : x-=3 : same as x=x-3
 * *= : x*=3 : same as x=x*3
 * /= : x/=3 : same as x=x/3
 * %= : x%=3 : same as x=x%3
 * &= : x&=3 : same as x=x&3
 * |= : x|=3 : same as x=x|3
 * ^= : x^=3 : same as x=x^3
 * >>= : x>>=3 : same as x=x>>3
 * <<= : x<<=3 : same as x=x<<3
 */

/* Comparision Operator:- (Return true, if condition will be true else return false)
 * == : equal to : x=y (x equals to y)
 * != : not equals to : x!=y (x not equals to y)
 * > : greater than : x>y (x greater than y)
 * >= : greater than equals to : x>=y (x greater than equals to y)
 * < : less than : x<y (x less than y)
 * <= : less than equals to : x<=y (x less than equals to y)
 */

/* Logical Operator:-
 * && : Logical And : return true if both satatement are true : x<5 && x<10
 * || : Logical Or : return true if any one of the statements is true : x<5 || x<4
 * ! : Logical Not : reverse the result, return false if the result is true and vice versa : !(x<5 && x<10)
 */




