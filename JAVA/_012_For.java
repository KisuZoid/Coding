public class _012_For {
    public static void main(String[] args) {
        //for loop : executes a block of code a limited amount of times 
    
        for(int i=1; i<=10; i++){
            System.out.println(i);
        }
        System.out.println("^^These are the first ten Natural Number!");
    }
}

/*
 * at starting of the loop, 
 * set i = 1;
 * and run the loop till condition be true
 * and at the end of every loop, increase the value of i by 1 and assign it to i. 
 */

/*
 * for(initialization, condition, action){...}
 * 
 * initialization : initialize the value once in a loop for first time.
 * condition : execute the loop while condition is true.
 * action : task that will be done everytime block repeats itself or after every iteration.
 */
